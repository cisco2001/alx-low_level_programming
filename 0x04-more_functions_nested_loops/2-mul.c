/**
 * mul - function that multipies two integers
 *
 * @a: parameter for passing the first integer
 *
 * @b: parameter for passing the second integer
 *
 * Description: program to calculate product of @a and @b
 *
 * Return: product of @a and @b
 */


int mul(int a, int b)
{
	return (a * b);
}
